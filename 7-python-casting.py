# Exemplos de Casting em Python

# ----- Inteiros -----
x = int(1)      # x será 1
y = int(2.8)    # y será 2 (parte decimal removida)
z = int("3")    # z será 3

print(x, y, z)
print(type(x), type(y), type(z))

# ----- Floats -----
x = float(1)       # x será 1.0
y = float(2.8)     # y será 2.8
z = float("3")     # z será 3.0
w = float("4.2")   # w será 4.2

print(x, y, z, w)
print(type(x), type(y), type(z), type(w))

# ----- Strings -----
x = str("s1")   # x será "s1"
y = str(2)      # y será "2"
z = str(3.0)    # z será "3.0"

print(x, y, z)
print(type(x), type(y), type(z))
